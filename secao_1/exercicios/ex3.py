valor_1 = input('digite um valor: ')
valor_2 = input('digite outro valor: ')

if valor_1 == valor_2:
    print('Os valores são esquivalentes.')
    
elif valor_1 > valor_2:
    print (f'O valor {valor_1} é maior'
           f'que o valor {valor_2}')
    
elif valor_2 > valor_1:
    print(f'O valor {valor_2} é maior'
          f'que o valor {valor_1}')

